CREATE TABLE LIBRARY_LCSET_SAMPLE(
  LIBRARY_LABEL VARCHAR2(40) NOT NULL,
  LIBRARY_ID NUMBER(19) NOT NULL,
  POSITION VARCHAR2(32),
  LCSET_SAMPLE VARCHAR2(40),
  BATCH_NAME VARCHAR2(40),
  MOLECULAR_BARCODE VARCHAR2(255),
  LIBRARY_TYPE VARCHAR2(255),
  LIBRARY_EVENT_ID NUMBER(19) NOT NULL,
  PRODUCT_ORDER_KEY VARCHAR2(255),
  PRODUCT_ORDER_SAMPLE VARCHAR2(40),
  LIBRARY_CREATION_DATE DATE NOT NULL );

CREATE INDEX IDX_LIBRARY_SAMPLE_LABEL
ON LIBRARY_LCSET_SAMPLE (LIBRARY_LABEL);

